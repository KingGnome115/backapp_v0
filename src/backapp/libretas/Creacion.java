    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnCrearActionPerformed
    {//GEN-HEADEREND:event_btnCrearActionPerformed

        String nombre = txtNombreLibreta.getText();
        String directorio = Opciones.directorio + "\\" + nombre;
        File libreta = new File(directorio);
        if (!libreta.exists())
        {
            libreta.mkdir();
            jLMensaje.setIcon(new javax.swing.ImageIcon(getClass().getResource("/iconos/check.png"))); // NOI18N
            jLMensaje.setText("nombre disponible");

            File carpeta = new File(Opciones.directorio);
            if (!carpeta.exists())
            {
                carpeta.mkdir();
            } else
            {
                FileFilter filtro = new FileFilter()
                {
                    @Override
                    public boolean accept(File fil)
                    {
                        return fil.isDirectory();
                    }
                };
                Opciones.lista = carpeta.listFiles(filtro);
            }

            directorio += "\\00";
            File hoja1 = new File(directorio);
            hoja1.mkdir();

            try
            {
                ObjectOutputStream file = new ObjectOutputStream(new FileOutputStream(directorio+ "\\Text.dat"));
                HojaLibreta obj = new HojaLibreta();
                file.writeObject(obj);
                file.close();
            } catch (IOException ex)
            {
                System.out.println("No se encontro archivo");
            }

        } else
        {
            jLMensaje.setIcon(new javax.swing.ImageIcon(getClass().getResource("/iconos/denegado.png"))); // NOI18N
            jLMensaje.setText("nombre no disponible");
        }
        txtNombreLibreta.setText("");


    }//GEN-LAST:event_btnCrearActionPerformed

